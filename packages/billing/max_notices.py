"""Тексты уведомлений в MAX о подписке (простой язык, 50+)."""

from __future__ import annotations


def notice_subscription_activated(*, plan_code: str) -> str:
    if plan_code == "business_marketer_490":
        return (
            "**Оплата прошла успешно.**\n\n"
            "Ваш тариф **бизнес-маркетолог** включён: больше картинок, посты для VK, "
            "без водяного знака на картинках.\n\n"
            "Подписка **продлевается сама** каждый период. Отключить автопродление можно в меню "
            "кнопкой **«Отменить автопродление»** — доступ останется до конца оплаченного срока.\n\n"
            "Выберите действие в меню."
        )
    return (
        "**Оплата прошла успешно.**\n\n"
        "Подписка **для себя** включена: больше «картиночных» действий и вопросов, без водяного знака.\n\n"
        "Подписка **продлевается сама**. Отменить автопродление можно в меню — доступ сохранится "
        "до конца уже оплаченного периода.\n\n"
        "Выберите в меню, что сделаем дальше."
    )


def notice_subscription_renewed(*, plan_code: str) -> str:
    _ = plan_code
    return (
        "**Подписка продлена.**\n\n"
        "Оплата следующего периода прошла успешно. Пользуйтесь тарифом как обычно — всё через меню."
    )


def notice_renewal_failed() -> str:
    return (
        "**Не удалось списать оплату за продление.**\n\n"
        "Проверьте карту в банке или лимиты. **Текущий доступ сохраняется до конца уже оплаченного срока.**\n\n"
        "Можно оформить оплату снова из меню «Подписка» или дождаться следующей попытки списания."
    )


def notice_subscription_cancelled() -> str:
    return (
        "**Автопродление отключено.**\n\n"
        "Дальше списаний не будет. **Вы пользуетесь тарифом до конца уже оплаченного срока**, "
        "потом снова будет бесплатный лимит.\n\n"
        "Если передумаете — можно снова оформить подписку в меню."
    )


def notice_access_expired() -> str:
    return (
        "**Срок подписки закончился.**\n\n"
        "Сейчас действуют бесплатные лимиты. Чтобы снова открыть полный доступ, оформите подписку в меню."
    )
