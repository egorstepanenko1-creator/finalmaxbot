"""Намерения поздравления: простые правила для пользователей 50+."""

from __future__ import annotations


def infer_greeting_intent(user_text: str) -> str:
    t = user_text.lower()
    if any(x in t for x in ("день рож", "др ", "д.р.", "именин", "родил")):
        return "birthday"
    if any(x in t for x in ("годовщин", "свадьб", "венчан")):
        return "anniversary"
    if any(
        x in t
        for x in (
            "новый год",
            "8 марта",
            "23 февраля",
            "пасх",
            "рождеств",
            "1 сентября",
            "выпускн",
        )
    ):
        return "holiday"
    return "universal"


def greeting_system_prompt(intent: str) -> str:
    base = (
        "Ты помощник для людей 50+. Напиши тёплое поздравление по-русски: простые слова, без канцелярита, "
        "4–8 коротких предложений. Без политики и без чужих имён, если пользователь их не указал."
    )
    hints = {
        "birthday": " Повод: день рождения — пожелай здоровья, радости, тепла в доме.",
        "anniversary": " Повод: годовщина или семейный праздник — уважительный, сердечный тон.",
        "holiday": " Повод: праздник — светлые пожелания, без навязчивых клише.",
        "universal": " Если мало деталей — универсальное доброе поздравление со здоровьем и удачей.",
    }
    return base + hints.get(intent, hints["universal"])


def build_greeting_card_image_prompt(user_text: str, intent: str) -> str:
    """Промпт для картинки-открытки (без текста на изображении)."""
    short = user_text.strip().replace("\n", " ")[:400]
    mood = {
        "birthday": "торт, цветы, свечи, уют",
        "anniversary": "цветы, двое людей силуэтом, золотые акценты",
        "holiday": "праздничные украшения, свет, тепло",
        "universal": "цветы, свет, уютная атмосфера",
    }.get(intent, "праздник, свет, тепло")
    return (
        f"Праздничная иллюстрация-открытка в мягком стиле, {mood}. "
        f"Без букв и надписей на картинке. Настроение: радость и тепло. "
        f"Идея от пользователя: {short}"
    )


def vk_post_image_prompt_from_post(post_text: str) -> str:
    p = post_text.strip().replace("\n", " ")[:600]
    return (
        "Яркая иллюстрация для рекламного поста ВКонтакте, без текста на изображении, "
        "привлекательная для клиентов малого бизнеса в России. Контекст поста: "
        f"{p}"
    )
